using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Calculator
{
    class Hitung
    {
        public int Penjumlahan(int x, int y)
        {
            return x + y;
        }

        public int Pengurangan(int x, int y)
        {
            return x - y;
        }
        public int Perkalian(int x, int y)
        {
            return x * y;
        }
        public int Pembagian(int x, int y)
        {
            return x / y;
        }
    }
}
